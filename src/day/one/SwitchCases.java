package day.one;

public class SwitchCases {
	
	
	public static void main(String[] args) {
		
		
		
		String str = "test11";
		
		switch(str){
			case "test":
				System.out.println("that is right");
				break;
			case "test1":
				System.out.println("that is wrong");
				break;
				default:
				System.out.println("default block");
		}
	}

}
